public class StrLngTpl {
    private String str;
    private Long lng;

    public StrLngTpl(String str, Long lng) {
        this.str = str;
        this.lng = lng;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public Long getLng() {
        return lng;
    }

    public void setLng(Long lng) {
        this.lng = lng;
    }
}
